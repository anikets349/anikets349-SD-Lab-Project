    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        // TODO add your handling code here:
        try {
            //Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project?useSSL=false", "root", "ani123");
            Statement stm = conn.createStatement();
            String bookName = issuedBooks.getSelectedItem().toString();
            String query = "select issueDate,dueDate,fine,paymentDate,isBookReturned,isFinePaid from issues,lib_book where issues.bookId=lib_book.bookId and lib_book.bookName='"+bookName+"' and userId='2GI19CS189';";
            ResultSet rs = stm.executeQuery(query);
            while (rs.next()) {
                //System.out.println("Book name : " + rs.getString("bookName"));
                System.out.println("Issue date : " + rs.getString("issueDate"));
                System.out.println("Due date : " + rs.getString("dueDate"));
                System.out.println("Fine : " + rs.getString("fine"));
                System.out.println("Payment date : " + rs.getString("paymentDate"));
                System.out.println("Book returned? : " + rs.getString("isBookReturned"));
                System.out.println("Fine paid? : " + rs.getString("isFinePaid"));
                
                dueDate.setText(rs.getString("dueDate"));
                issueDate.setText(rs.getString("issueDate"));
                fine.setText(rs.getString("fine"));
                if (rs.getString("isBookReturned").equals("1")) {
                    status.setText("returned");
                }
                else {
                    status.setText("to be returned");
                }
            }
            conn.close();
        }
        catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
        }
        
    }//GEN-LAST:event_btnActionPerformed

