    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jRadioButton1.setActionCommand("user");
        jRadioButton2.setActionCommand("staff");
        jRadioButton3.setActionCommand("admin");
        String password = jPasswordField1.getText();
        String userName = jTextField1.getText();
        String loginType = buttonGroup1.getSelection().getActionCommand();
        String sql = "select * from login where id=? and password=? and loginType=?";
        try{
            //Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project", "root", "ani123");
            pst=con.prepareStatement(sql);
            pst.setString(1,userName);
            pst.setString(2,password);
            pst.setString(3,loginType);
            rs = pst.executeQuery();
            if(rs.next())
            {
                if("Admin".equalsIgnoreCase(loginType))
                    new admin_menu().setVisible(true);
                else if("staff".equals(loginType))
                    new staff_menu(userName).setVisible(true);
                else
                    new user_menu(userName).setVisible(true);
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "username and password do not matched");
            }
        }
        catch(SQLException | HeadlessException ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

