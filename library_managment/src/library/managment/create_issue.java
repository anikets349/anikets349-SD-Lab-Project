    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String bookId = books.getSelectedItem().toString();
        System.out.println(bookId);
        String copyNo = jTextField3.getText();
        String userId = users.getSelectedItem().toString();
        String issueDate = (String)jComboBox3.getSelectedItem() + "-" + (String)jComboBox2.getSelectedItem() + "-" + (String)jComboBox1.getSelectedItem();
        int maxIssuePeriod, maxIssues, curIssues;
        try
        {
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "ani123");
        Statement stmt = con.createStatement();
        String q = "select bookId from lib_book where bookId = '" +bookId+"';";
        ResultSet rs = stmt.executeQuery(q);
        if(!rs.next())
            JOptionPane.showMessageDialog(this, "THIS BOOK DOES NOT EXIST");
        else
        {
            q = "select userId from lib_users where userId = '" +userId+"';";
            rs = stmt.executeQuery(q);
            if(!rs.next())
                JOptionPane.showMessageDialog(this, "THIS USER DOES NOT EXIST");
            else
            {
                q = "select * from issues where bookId='"+bookId+"' AND copyNo='"+copyNo+"' AND isBookReturned=0;";
                rs = stmt.executeQuery(q);
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(this, "THIS COPY OF THE BOOK HAS ALREADY BEEN ISSUED");
                }
                else
                {
                    q = "select * from lib_users inner join user_types on lib_users.userType = user_types.userType where userId='"+userId+"';";
                    rs = stmt.executeQuery(q);
                    if(rs.next())
                    {
                        maxIssues = Integer.parseInt(rs.getString("maxIssues"));
                        maxIssuePeriod = Integer.parseInt(rs.getString("maxIssuePeriod"));
                        q = "select count(*) as curIssues from issues where userId='" + userId + "' AND isBookReturned=1";
                        rs = stmt.executeQuery(q);
                        if(rs.next())
                            curIssues = Integer.parseInt(rs.getString("curIssues"));
                        else
                            curIssues = 0; 
                        if(curIssues < maxIssues)
                        {
                            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                            Calendar c = Calendar.getInstance();
                            c.setTime(sdf.parse(issueDate));
                            c.add(Calendar.DATE, maxIssuePeriod);  // number of days to add
                            String dueDate = sdf.format(c.getTime());  // dt is now the new date

                            q = "insert into issues (bookId, copyNo, userId, issueDate, dueDate, issueStaffId) values('"+bookId+"',"+copyNo+",'"+userId+"','"+issueDate+"','"+dueDate+"','"+staffId+"');";
                            stmt.executeUpdate(q);
                            JOptionPane.showMessageDialog(this, "The Book is now Issued");
                        }
                        else 
                        {
                            JOptionPane.showMessageDialog(this, "Maximum Issues Reached");
                        }
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "INVALID USER TYPE");
                    }
                }
            }
        }
        //jTextField1.setText("");
        jTextField3.setText("");
        //users.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox3.setSelectedIndex(0);
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

